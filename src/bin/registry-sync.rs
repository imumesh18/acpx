fn render_registry_functions(
    rendered: &mut String,
    agents: &[RegistryAgent],
) -> Result<(), Box<dyn Error>> {
    writeln!(rendered, "#[must_use]")?;
    writeln!(
        rendered,
        "pub fn generated_agent_servers() -> Vec<GeneratedAgentServer> {{"
    )?;
    writeln!(rendered, "    vec![")?;
    for agent in agents {
        writeln!(rendered, "        {}(),", function_name(&agent.id))?;
    }
    writeln!(rendered, "    ]")?;
    writeln!(rendered, "}}")?;
    writeln!(rendered)?;
    writeln!(rendered, "#[must_use]")?;
    writeln!(
        rendered,
        "pub fn generated_agent_server(id: &str) -> Option<GeneratedAgentServer> {{"
    )?;
    writeln!(rendered, "    match id {{")?;
    for agent in agents {
        writeln!(
            rendered,
            "        {} => Some({}()),",
            rust_string_literal(&agent.id),
            function_name(&agent.id)
        )?;
    }
    writeln!(rendered, "        _ => None,")?;
    writeln!(rendered, "    }}")?;
    writeln!(rendered, "}}")?;
    writeln!(rendered)?;
    writeln!(rendered, "#[must_use]")?;
    writeln!(
        rendered,
        "pub fn registry_agent_servers() -> Vec<RegistryAgentServer> {{"
    )?;
    writeln!(rendered, "    generated_agent_servers()")?;
    writeln!(rendered, "}}")?;
    writeln!(rendered)?;
    writeln!(rendered, "#[must_use]")?;
    writeln!(
        rendered,
        "pub fn registry_agent_server(id: &str) -> Option<RegistryAgentServer> {{"
    )?;
    writeln!(rendered, "    generated_agent_server(id)")?;
    writeln!(rendered, "}}")?;

    Ok(())
}

fn render_agent_function(
    rendered: &mut String,
    agent: &RegistryAgent,
) -> Result<(), Box<dyn Error>> {
    writeln!(rendered, "#[must_use]")?;
    writeln!(
        rendered,
        "pub fn {}() -> GeneratedAgentServer {{",
        function_name(&agent.id)
    )?;
    writeln!(rendered, "    GeneratedAgentServer {{")?;
    write!(
        rendered,
        "        metadata: AgentMetadata::new({}, {}, {})",
        rust_string_literal(&agent.id),
        rust_string_literal(&agent.name),
        rust_string_literal(&agent.version)
    )?;
    if !agent.description.is_empty() {
        write!(
            rendered,
            ".description({})",
            rust_string_literal(&agent.description)
        )?;
    }
    if let Some(icon) = &agent.icon {
        write!(rendered, ".icon({})", rust_string_literal(icon))?;
    }
    writeln!(rendered, ",")?;
    writeln!(
        rendered,
        "        repository: {},",
        render_optional_owned_string(agent.repository.as_deref())
    )?;
    writeln!(
        rendered,
        "        authors: vec![{}],",
        render_string_list(&agent.authors)
    )?;
    writeln!(
        rendered,
        "        license: {}.to_owned(),",
        rust_string_literal(&agent.license)
    )?;
    writeln!(rendered, "        distribution: GeneratedDistribution {{")?;
    writeln!(
        rendered,
        "            binary: vec![{}],",
        render_binary_targets(&agent.distribution.binary)
    )?;
    writeln!(
        rendered,
        "            npx: {},",
        render_package_distribution(agent.distribution.npx.as_ref(), "Npx")
    )?;
    writeln!(
        rendered,
        "            uvx: {},",
        render_package_distribution(agent.distribution.uvx.as_ref(), "Uvx")
    )?;
    writeln!(rendered, "        }},")?;
    writeln!(rendered, "    }}")?;
    writeln!(rendered, "}}")?;
    writeln!(rendered)?;

    Ok(())
}

fn render_binary_targets(binary: &BTreeMap<String, BinaryTarget>) -> String {
    let mut rendered = Vec::new();
    for (target, details) in binary {
        rendered.push(format!(
            "GeneratedBinaryTarget {{ target: {}.to_owned(), archive: {}.to_owned(), cmd: {}.to_owned() }}",
            rust_string_literal(target),
            rust_string_literal(&details.archive),
            rust_string_literal(&details.cmd),
        ));
    }

    rendered.join(", ")
}

fn render_package_distribution(package: Option<&PackageDistribution>, manager: &str) -> String {
    let Some(package) = package else {
        return "None".to_owned();
    };

    let args = render_string_list(&package.args);
    let env = package
        .env
        .iter()
        .map(|(key, value)| {
            format!(
                "({}.to_owned(), {}.to_owned())",
                rust_string_literal(key),
                rust_string_literal(value)
            )
        })
        .collect::<Vec<_>>()
        .join(", ");

    format!(
        "Some(GeneratedPackageDistribution {{ manager: GeneratedPackageManager::{manager}, package: {}.to_owned(), args: vec![{args}], env: vec![{env}] }})",
        rust_string_literal(&package.package)
    )
}

fn render_string_list(items: &[String]) -> String {
    items
        .iter()
        .map(|item| format!("{}.to_owned()", rust_string_literal(item)))
        .collect::<Vec<_>>()
        .join(", ")
}

fn render_optional_owned_string(value: Option<&str>) -> String {
    value.map_or_else(
        || "None".to_owned(),
        |value| format!("Some({}.to_owned())", rust_string_literal(value)),
    )
}

fn function_name(id: &str) -> String {
    let mut output = String::with_capacity(id.len() + 8);
    output.push_str("agent_");

    for ch in id.chars() {
        if ch.is_ascii_alphanumeric() {
            output.push(ch.to_ascii_lowercase());
        } else {
            output.push('_');
        }
    }

    output
}

fn rust_string_literal(value: &str) -> String {
    format!("{value:?}")
}

#[cfg(test)]
mod tests {
    use super::{RegistryDocument, render_registry_module};

    #[test]
    fn registry_render_is_idempotent_for_sample_fixture() {
        let registry: RegistryDocument =
            serde_json::from_str(include_str!("../../tests/fixtures/registry-sample.json"))
                .expect("fixture should parse");

        let first = render_registry_module(&registry).expect("first render should succeed");
        let second = render_registry_module(&registry).expect("second render should succeed");

        assert_eq!(first, second);
        assert!(first.contains("pub const REGISTRY_VERSION: &str = \"1.0.0\";"));
        assert!(first.contains("pub type RegistryAgentServer = GeneratedAgentServer;"));
        assert!(first.contains("pub fn registry_agent_servers() -> Vec<RegistryAgentServer> {"));
        assert!(first.contains("pub fn agent_alpha_agent()"));
        assert!(first.contains("pub fn agent_zeta_agent()"));
        assert!(first.contains("Self::Npx => &[\"--yes\"]"));
        assert!(first.contains("repository: None,"));
        assert!(first.find("pub fn agent_alpha_agent()") < first.find("pub fn agent_beta_agent()"));
        assert!(
            first.find("GeneratedPackageManager::Npx") < first.find("GeneratedPackageManager::Uvx")
        );
    }
}
